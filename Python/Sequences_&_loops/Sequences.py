#DATA STRUCTURE
#List - sequnce of mutable values
#Tuple - sequence of immutable values
#Set - unordered collection of unique values
#Dictionary - unordered collection of key-value pairs

#Sequence
name = ['Harry', 'Ron', 'Hermione']
