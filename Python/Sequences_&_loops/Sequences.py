#Tuple
coordinateX = 10.0
coordinateY = 20.0

coordinate = (coordinateX, coordinateY)
print(coordinate[0])


#Dictionary
person = {'name': 'John Doe', 'age': 30, 'city': 'New York'}
print(person['name'])